package com.cherrypicks.tcc.cms.dto;

import java.util.Date;
import java.util.List;

public class CategoryDTO extends BaseObject {

	private static final long serialVersionUID = 1359849809674562226L;

	private Long id;
	private Long parentId;
	private Integer sortOrder;
	private String name;
	private String image;
	private String descr;
	private String langCode;
	private Date createdTime;
	private String createdBy;
	private Date updatedTime;
	private String updatedBy;
	private List<CategoryDTO> subCategoryDTO;

	public Long getId() {
		return id;
	}

	public void setId(Long id) {
		this.id = id;
	}

	public Long getParentId() {
		return parentId;
	}

	public void setParentId(Long parentId) {
		this.parentId = parentId;
	}

	public Integer getSortOrder() {
		return sortOrder;
	}

	public void setSortOrder(Integer sortOrder) {
		this.sortOrder = sortOrder;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getImage() {
		return image;
	}

	public void setImage(String image) {
		this.image = image;
	}

	public String getDescr() {
		return descr;
	}

	public void setDescr(String descr) {
		this.descr = descr;
	}

	public String getLangCode() {
		return langCode;
	}

	public void setLangCode(String langCode) {
		this.langCode = langCode;
	}

	public Date getCreatedTime() {
		return createdTime;
	}

	public void setCreatedTime(Date createdTime) {
		this.createdTime = createdTime;
	}

	public String getCreatedBy() {
		return createdBy;
	}

	public void setCreatedBy(String createdBy) {
		this.createdBy = createdBy;
	}

	public Date getUpdatedTime() {
		return updatedTime;
	}

	public void setUpdatedTime(Date updatedTime) {
		this.updatedTime = updatedTime;
	}

	public String getUpdatedBy() {
		return updatedBy;
	}

	public void setUpdatedBy(String updatedBy) {
		this.updatedBy = updatedBy;
	}

	public List<CategoryDTO> getSubCategoryDTO() {
		return subCategoryDTO;
	}

	public void setSubCategoryDTO(List<CategoryDTO> subCategoryDTO) {
		this.subCategoryDTO = subCategoryDTO;
	}

}
